    private void tampilData() {
        defaultTableModel = new DefaultTableModel();
        defaultTableModel.addColumn("ID");
        defaultTableModel.addColumn("Soal");
        defaultTableModel.addColumn("Pil_A");
        defaultTableModel.addColumn("Pil_B");
        defaultTableModel.addColumn("Pil_C");
        defaultTableModel.addColumn("Pil_D");
        defaultTableModel.addColumn("Kunci");
        tabelSoal.setModel(defaultTableModel);

        try {
            String query = "SELECT * FROM soal";
            statement = (PreparedStatement) connection.prepareStatement(query);
            ResultSet result = statement.executeQuery();
            while (result.next()) {
                defaultTableModel.addRow(new Object[]{
                    result.getString("id"),
                    result.getString("soal"),
                    result.getString("pil_a"),
                    result.getString("pil_b"),
                    result.getString("pil_c"),
                    result.getString("pil_d"),
                    result.getString("kunci"),});
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

