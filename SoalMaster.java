    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String soal = soalTextField.getText();
        String pil_a = pilATextField.getText();
        String pil_b = pilBTextField.getText();
        String pil_c = pilCTextField.getText();
        String pil_d = pilDTextField.getText();
        String kunci = kunciTextField.getText();

        try {
            String query = "INSERT INTO soal(soal, pil_a, pil_b, pil_c, pil_d, kunci, created_by) values(?,?,?,?,?,?,?)";
            statement = (PreparedStatement) connection.prepareStatement(query);
            statement.setString(1, soal);
            statement.setString(2, pil_a);
            statement.setString(3, pil_b);
            statement.setString(4, pil_c);
            statement.setString(5, pil_d);
            statement.setString(6, kunci);
            statement.setInt(7, userId);
            statement.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            tampilData();
        } catch (SQLException e) {
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        String soal = soalTextField.getText();
        String pil_a = pilATextField.getText();
        String pil_b = pilBTextField.getText();
        String pil_c = pilCTextField.getText();
        String pil_d = pilDTextField.getText();
        String kunci = kunciTextField.getText();
        int baris = tabelSoal.getSelectedRow();

        try {
            String query = "UPDATE soal set soal = ?, pil_a = ?, pil_b = ?, pil_c = ?, pil_d = ?, kunci = ? where id = ?";
            statement = (PreparedStatement) connection.prepareStatement(query);
            statement.setString(1, soal);
            statement.setString(2, pil_a);
            statement.setString(3, pil_b);
            statement.setString(4, pil_c);
            statement.setString(5, pil_d);
            statement.setString(6, kunci);
            statement.setInt(7, Integer.parseInt(defaultTableModel.getValueAt(baris, 0).toString()));
            statement.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            tampilData();
        } catch (SQLException e) {
        }
    }//GEN-LAST:event_editButtonActionPerformed

